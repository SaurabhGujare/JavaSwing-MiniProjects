    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)vitalSignTbl.getModel();
        //remove all the row that are displayed already
        dtm.setRowCount(0);
        for(VitalSigns vs: vsh.getVitalSignHistory())
        {
            Object row[]=new Object[2];
            row[0] = vs;
            row[1] = vs.getPulse();
            dtm.addRow(row);
        }
    }
